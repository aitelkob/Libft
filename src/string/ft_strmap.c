#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char			*str;
	size_t			i;

	i = 0;
	if (s == NULL)
		return (NULL);
	i = ft_strlen(s);
	str = (char*)malloc(sizeof(char) * (i + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = (*f)(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
