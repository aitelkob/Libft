#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	temps;

	j = 0;
	while (str[j] != '\0')
	{
		j++;
	}
	j--;
	i = 0;
	while (i < j)
	{
		temps = str[i];
		str[i] = str[j];
		str[j] = temps;
		i++;
		j--;
	}
	return (str);
}
